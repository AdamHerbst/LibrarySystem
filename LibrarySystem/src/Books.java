
public class Books {
	private String bookName;
	private String bookAuthor;
	private int quantity;
	
	
	public void setBookName(String iBook) {
		bookName = iBook;
	}
	public String getBookName() {
		return bookName;
	}
	public void setQuantity(int iQuantity) {
		quantity = iQuantity;
	}
	public int getQuantity() {
		return quantity;
	}
	public void setBookAuthor(String iAuthor) {
		bookAuthor = iAuthor;
	}
	public String getBookAuthor() {
		return bookAuthor;
	}
}
